#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	t_uc	*cur;

	cur = (t_uc*)s;
	while (n--)
	{
		if (*cur == (t_uc)c)
			return (cur);
		++cur;
	}
	return (0);
}
